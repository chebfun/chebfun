% Chebfun extends familiar powerful methods of numerical computation
% involving numbers to continuous or piecewise-continuous functions.  It
% also implements continuous analogues of linear algebra notions like the
% QR decomposition and the SVD.  The mathematical basis of Chebfun combines
% tools of Chebyshev expansions, fast Fourier transform, barycentric
% interpolation, Clenshaw-Curtis quadrature, and recursive zerofinding.
%
